# Problem 2: Create a list containing the numbers 0 to 10 using a while loop

L = []          # Empty list
counter = 0     # Start counting at 0

while counter <= 10:     # Loop stops when counter becomes greater than 10
    L.append(counter)    # Add current number to the list
    counter += 1         # Increase counter by 1

print(L)   # Confirm output
